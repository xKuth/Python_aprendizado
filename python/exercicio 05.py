letra = input('Digite algo ')
print('Seu tipo primitivo é\033[0;31m',type(letra), '\033[m')
print('Pode ser convertida para numero ?\033[0;31m', letra.isnumeric(), '\033[m')
print('Contem letras ou numeros ?\033[0;31m', letra.isalnum(), '\033[m')
print('Todas as letras estão maiúsculas ?\033[0;31m', letra.isupper(), '\033[m')
print('Todas as letras estão minúsculas ?\033[0;31m', letra.islower(), '\033[m')
print('Contém casas decimais ?\033[0;31m', letra.isdecimal(), '\033[m')
print('Contém apenas espaço ?\033[0;31m', letra.isspace(), '\033[m')
print('Comtém apenas letras ?\033[0;31m', letra.isalpha(), '\033[m')
print('É uma string vazia ou contem ASCII\033[0;31m', letra.isascii(), '\033[m')
print('Todos os Os caracteres são digitos e contém pelo menos um caractere?\033[0;31m',letra.isdigit(), '\033[m')
print('Todos os caracteres são validos ?\033[0;31m', letra.isidentifier(), '\033[m')
print('Todos os caracteres podem ser impressos ou é uma string vazia ?\033[0;31m',letra.isprintable(), '\033[m')
print('Existe pelo menos um caractere maiusculo ou no começo ?\033[0;31m',letra.istitle(), '\033[m')